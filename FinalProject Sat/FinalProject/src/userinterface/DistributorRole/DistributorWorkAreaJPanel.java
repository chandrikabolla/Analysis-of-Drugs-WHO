    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateTable();

    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        DrugOrderWorkRequest request = (DrugOrderWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);

        request.setStatus("Processing");

        ProcessWorkRequestJPanel processWorkRequestJPanel = new ProcessWorkRequestJPanel(userProcessContainer, enterprise,request);
        userProcessContainer.add("processWorkRequestJPanel", processWorkRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_processJButtonActionPerformed

    private void reqMedicineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reqMedicineBtnActionPerformed
        // TODO add your handling code here:
        String message = messageTxtFld.getText();

        SalesWorkRequest request = new SalesWorkRequest();
        request.setMessage(message);
        request.setSender(userAccount);
        request.setStatus("Sent");
        request.setCartSize(Integer.parseInt(cartSizejTextField.getText()));
        request.setRequestedEnterprise(enterprise);
        ManufactureEnterprise mEnterprise=network.getEnterpriseDirectory().searchManufacturingEnterprise(manufacturerCombo.getSelectedItem().toString());
        SalesOrganization org = null;
        for (Organization organization : mEnterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof SalesOrganization){
                org = (SalesOrganization)organization;
                break;
            }
        }
        if (org!=null){
            org.getSalesWorkQueue().getSalesWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
            
            
        }
    }//GEN-LAST:event_reqMedicineBtnActionPerformed


    private void populateDrugTable(){
        DefaultTableModel model = (DefaultTableModel)drugTable.getModel();
        
        model.setRowCount(0);
        
        for(Drug drug : enterprise.getDrugList()){
            Object[] row = new Object[1];
            row[0] = drug;
            
            model.addRow(row);
        }
        
    }
    private void populateManufacturerCombo(){
        
        manufacturerCombo.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Manufacture))
            {
                manufacturerCombo.addItem(enterprise.getName());
            }
        }
    }
    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : distributingOrganization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getUsername();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getUsername();
            row[3] = request.getStatus();
            
            model.addRow(row);
        }
    }
